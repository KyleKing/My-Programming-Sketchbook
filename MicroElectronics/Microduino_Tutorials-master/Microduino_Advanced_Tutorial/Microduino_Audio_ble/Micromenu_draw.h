//主界面，可自由定义
void drawMain()
{
  u8g.drawXBMP( 4, 6, u8g_logo_width, u8g_logo_height, u8g_logo_bits);

  //setFont_L;
  //u8g.setFont(u8g_font_9x15Br);
  //u8g.setFont(u8g_font_9x18Br);
  u8g.setFont(u8g_font_courB24n);
  u8g.setFontRefHeightText();

  u8g.setPrintPos(42, 8+u8g.getFontAscent()-u8g.getFontDescent());
  if(Hour<10)
    u8g.print("0");
  u8g.print(Hour);

  u8g.setPrintPos(76, 8+u8g.getFontAscent()-u8g.getFontDescent());
  u8g.print(Second%2 ? ":" : " ");

  u8g.setPrintPos(91, 8+u8g.getFontAscent()-u8g.getFontDescent());
  if(Minute<10)
    u8g.print("0");
  u8g.print(Minute);

  setFont_L;
  u8g.setFontRefHeightText();
  u8g.setPrintPos(61, 60);
  if(alarm_hour<10)
    u8g.print("0");
  u8g.print(alarm_hour);
  u8g.setPrintPos(81, 60);
  u8g.print(":");
  u8g.setPrintPos(91, 60);
  if(alarm_minute<10)
    u8g.print("0");
  u8g.print(alarm_minute);

  if(alarm_switch)
  {
    u8g.setPrintPos(6, 60);
    u8g.print("ON");
  }
  else
  {
    u8g.setPrintPos(2, 60);
    u8g.print("OFF");
  }
}



